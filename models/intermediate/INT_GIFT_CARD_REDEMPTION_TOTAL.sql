SELECT
         ITEMS.INVOICE_ID
        ,SUM(TRANSACTIONS.AMOUNT_IN_CENTS) / -100 AS TOTAL_REDEMPTION_AMOUNT
    FROM {{ ref('STG_GIFT_CARD_TRANSACTION_INVOICE_VW') }} REDEMPTIONS
    INNER JOIN {{ ref('STG_GIFT_CARD_TRANSACTION_VW') }} TRANSACTIONS
        ON REDEMPTIONS.GIFT_CARD_TRANSACTION_ID = TRANSACTIONS.ID
    INNER JOIN {{ ref('STG_STRIPE_INVOICE_LINE_ITEMS') }} ITEMS
        ON REDEMPTIONS.INVOICE_LINE_ITEM_ID = ITEMS.INVOICE_LINE_ITEM_ID
    GROUP BY
         ITEMS.INVOICE_ID